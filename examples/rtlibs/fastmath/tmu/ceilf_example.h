typedef float float32_t;
__attribute__((section("kernel_opt")))
static float32_t ceilf_opt(float32_t in)
{	
	float32_t out = (in - __builtin_c29_i32_fracf32_m(in));
    out = (__builtin_c29_i32_fracf32_m(in) <= 0) ? out: out+1.0f;
    return out;
	
}